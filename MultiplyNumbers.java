import java.util.Scanner;

public class MultiplyNumbers {
    public static void main(String[] args) {
        // Hardcoded numbers
        double num1 = 5;
        double num2 = 10;

        // Multiply the numbers
        double result = num1 * num2;

        // Display the result
        System.out.println("The multiplication of " + num1 + " and " + num2 + " is: " + result);
    }
}

